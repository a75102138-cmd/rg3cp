export function isValidBddCategory(v: string): v is BddCategory {
  return BDD_CATEGORIES.includes(v as BddCategory);
}

export function isValidSubcategory(category: string, subCategory?: string | null): boolean {
  if (!subCategory) return true;
  if (!isValidBddCategory(category)) return false;
  return BDD_SUBCATEGORIES[category].includes(subCategory);
}
